// ─── Style 3: Oscilloscope (Waveform) ──────────────
function drawOscilloscope(width, height) {
// Standard time-domain waveform
let waveMax = 0;
for (let i = 0; i < state.waveArray.length; i++) {
const val = Math.abs(state.waveArray[i] - 128) / 128;
if (val > waveMax) waveMax = val;
}
updateWavePeak(waveMax);

const activeHue = getBaseHue();
const sat = state.currentTheme === 'monochrome' ? '0%' : '80%';
const satActive = state.currentTheme === 'monochrome' ? '0%' : '90%';

const drawWavePath = (arr, yOffset, yHeight) => {
const sliceWidth = width * 1.0 / arr.length;
let x = 0;
state.ctx.beginPath();
for (let i = 0; i < arr.length; i++) {
const v = arr[i] / 128.0; // 0.0 to 2.0
const y = yOffset + ((v - 1.0) * yHeight / 2) * state.sensitivityMultiplier;
if (i === 0) {
state.ctx.moveTo(x, y);
} else {
state.ctx.lineTo(x, y);
}
x += sliceWidth;
}
};

const renderWaves = () => {
if (state.isStereo && state.waveArrayL && state.waveArrayR) {
// Left channel
state.ctx.strokeStyle = `hsla(${activeHue}, ${sat}, 60%, 0.9)`;
drawWavePath(state.waveArrayL, height * 0.25, height * 0.8);
state.ctx.stroke();
// Right channel
state.ctx.strokeStyle = `hsla(${(activeHue + 30) % 360}, ${sat}, 60%, 0.9)`; // slight hue shift for right
drawWavePath(state.waveArrayR, height * 0.75, height * 0.8);
state.ctx.stroke();
} else {
state.ctx.strokeStyle = `hsla(${activeHue}, ${satActive}, ${55 + state.energy * 20}%, 0.9)`;
drawWavePath(state.waveArray, height / 2, height * 1.6);
state.ctx.stroke();
}
};

// Draw lines with glow
state.ctx.lineWidth = Math.max(0.5, state.oscLineWidth * 0.4);
state.ctx.shadowBlur = state.oscGlowIntensity * 20;
state.ctx.shadowColor = `hsla(${activeHue}, ${sat}, 60%, 1.0)`;
renderWaves();
state.ctx.shadowBlur = 0;
}

